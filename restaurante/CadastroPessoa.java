    private void salvarRegistro() {
        Endereco endereco;
        ArrayList<String> telefones;
        ArrayList<String> alergias;
        Date dataNascimento;

        if (this.validarCampos() == false) {
            return;
        }

        if (jTextFieldDataNascimento.getText().length() == 0) {
            dataNascimento = null;
        } else {
            try {
                dataNascimento = dateFormat.parse(jTextFieldDataNascimento.getText());
            } catch (ParseException ex) {
                exibirInformacao("Falha ao gravar a data de nascimento: " + ex.toString());
                return;
            }
        }

        endereco = new Endereco();
        endereco.setBairro(jTextFieldBairro.getText());
        endereco.setCep(jTextFieldCep.getText());
        endereco.setCidade(jTextFieldCidade.getText());
        endereco.setComplemento(jTextFieldComplemento.getText());
        endereco.setEstado((String) jComboBoxEstado.getSelectedItem());
        endereco.setLogradouro(jTextFieldLogradouro.getText());
        endereco.setNumero(jTextFieldNumero.getText());
        endereco.setPais(jTextFieldPais.getText());

        telefones = new ArrayList<String>();
        for (int i = 0; i < telefonesListModel.size(); i++) {
            telefones.add(telefonesListModel.getElementAt(i).toString());
        }

        alergias = new ArrayList<String>();
        for (int i = 0; i < alergiaListModel.size(); i++) {
            alergias.add(alergiaListModel.getElementAt(i).toString());
        }

        if (novoRegistro == true) {
            umaPessoa = new Pessoa(jTextFieldNome.getText());
        } else {
            umaPessoa.setNome(jTextFieldNome.getText());
        }
        umaPessoa.setEndereco(endereco);
        umaPessoa.setTelefones(telefones);
        umaPessoa.setAlergias(alergias);
        umaPessoa.setDataNascimento(dataNascimento);
        umaPessoa.setAltura(Double.parseDouble(jTextFieldAltura.getText()));
        umaPessoa.setNomeMae(jTextFieldNomeMae.getText());
        umaPessoa.setNomePai(jTextFieldNomePai.getText());
        umaPessoa.setPeso(Double.parseDouble(jTextFieldPeso.getText()));
        umaPessoa.setCpf(jTextFieldCpf.getText());
        umaPessoa.setRg(jTextFieldRg.getText());
        umaPessoa.setIdade(Integer.parseInt(jTextFieldIdade.getText()));

        switch (jComboBoxSexo.getSelectedIndex()) {
            case SEXO_MASCULINO_INDICE:
                umaPessoa.setSexo(SEXO_MASCULINO_VALOR);
                break;
            case SEXO_FEMININO_INDICE:
                umaPessoa.setSexo(SEXO_FEMININO_VALOR);
                break;
        }


        if (novoRegistro == true) {
            controlePessoa.adicionar(umaPessoa);
        }
        modoAlteracao = false;
        novoRegistro = false;
        this.carregarListaJudocas();
        this.habilitarDesabilitarCampos();
    }
    
        private void exibirInformacao(String info) {
        JOptionPane.showMessageDialog(this, info, "Atenção", JOptionPane.INFORMATION_MESSAGE);
    }

    private void carregarListaJudocas() {
        ArrayList<Pessoa> listaPessoas = controlePessoa.getListaPessoas();
        DefaultTableModel model = (DefaultTableModel) jTableListaPessoas.getModel();
        model.setRowCount(0);
        for (Pessoa b : listaPessoas) {
            model.addRow(new String[]{b.getNome(), b.getCpf()});
        }
        jTableListaPessoas.setModel(model);
    }

