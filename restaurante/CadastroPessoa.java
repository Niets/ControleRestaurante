    private void preencherCampos() {
        ArrayList<String> telefones;
        ArrayList<String> alergias;

        jTextFieldAltura.setText(Double.toString(umaPessoa.getAltura()));
        jTextFieldBairro.setText(umaPessoa.getEndereco().getBairro());
        jTextFieldCep.setText(umaPessoa.getEndereco().getCep());
        jTextFieldCidade.setText(umaPessoa.getEndereco().getCidade());
        jTextFieldComplemento.setText(umaPessoa.getEndereco().getComplemento());
        jTextFieldCpf.setText(umaPessoa.getCpf());
        if (umaPessoa.getDataNascimento() == null) {
            jTextFieldDataNascimento.setText(null);
        } else {
            jTextFieldDataNascimento.setText(dateFormat.format(umaPessoa.getDataNascimento()));
        }
        jComboBoxEstado.setSelectedItem(umaPessoa.getEndereco().getEstado());
        jTextFieldLogradouro.setText(umaPessoa.getEndereco().getLogradouro());
        jTextFieldNome.setText(umaPessoa.getNome());
        jTextFieldNomeMae.setText(umaPessoa.getNomeMae());
        jTextFieldNomePai.setText(umaPessoa.getNomePai());
        jTextFieldNumero.setText(umaPessoa.getEndereco().getNumero().toString());
        jTextFieldPais.setText(umaPessoa.getEndereco().getPais());
        jTextFieldPeso.setText(Double.toString(umaPessoa.getPeso()));
        jTextFieldRg.setText(umaPessoa.getRg());

        telefonesListModel.clear();
        telefones = umaPessoa.getTelefones();
        if(telefones != null){    
            for (String t : telefones) {
                telefonesListModel.addElement(t);
            }
        }

        alergiaListModel.clear();
        alergias = umaPessoa.getAlergias();
        if(alergias != null){    
            for (String p : alergias) {
                alergiaListModel.addElement(p);
            }
        }
        

        switch (umaPessoa.getSexo()) {
            case SEXO_MASCULINO_VALOR:
                jComboBoxSexo.setSelectedIndex(SEXO_MASCULINO_INDICE);
                break;
            case SEXO_FEMININO_VALOR:
                jComboBoxSexo.setSelectedIndex(SEXO_FEMININO_INDICE);
                break;
        }
    }

    private boolean validarCampos() {
        if (jTextFieldNome.getText().trim().length() == 0) {
            this.exibirInformacao("O valor do campo 'Nome' não foi informado.");
            jTextFieldNome.requestFocus();
            return false;
        }
        if (jTextFieldDataNascimento.getText().length() != 0) {
            try {
                dateFormat.parse(jTextFieldDataNascimento.getText());
            } catch (ParseException ex) {
                this.exibirInformacao("O valor do campo 'Data de Nascimento' é inválido.");
                jTextFieldDataNascimento.requestFocus();
                return false;
            }
        }
        try {
            Double.parseDouble(jTextFieldAltura.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Altura' é inválido.");
            jTextFieldAltura.requestFocus();
            return false;
        }
        try {
            Double.parseDouble(jTextFieldPeso.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Peso' é inválido.");
            jTextFieldPeso.requestFocus();
            return false;
        }
        if (!jTextFieldNumero.getText().equals("")) {
            try {
                Integer.parseInt(jTextFieldNumero.getText());
            } catch (Exception ex) {
                this.exibirInformacao("O valor do campo 'Número' é inválido.");
                jTextFieldNumero.requestFocus();
                return false;
            }
        }
        return true;
    }

